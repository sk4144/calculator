

/**This class represents PostFixException error that is raised
 * when a problem occurs in ExpressionTools or Calculator class
 *
 * @author Suebin Kim
 * @version 11/18/15
 */


public class PostFixException extends Exception {

	public PostFixException() {
		super();
	}

	public PostFixException(String message) {
		super(message);
	}
}
